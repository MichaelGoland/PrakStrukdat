
angka1 = int(input("Masukkan angka pertama : "))
angka2 = int(input("Masukkan angka kedua : "))

#tambah
print("{} + {} = ".format(angka1,angka2))
print(angka1 + angka2)

#pengurangan
print("{} - {} = ".format(angka1,angka2))
print(angka1-angka2)

#perkalian
print("{} X {} = ".format(angka1,angka2))
print(angka1 * angka2)

#pembagian
print("{} : {} = ".format(angka1,angka2))
print(int(angka1 / angka2))



